    private void jButton1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton1MouseClicked
        // TODO add your handling code here:
        // Creamos en menu y estrablecemos las comidas y bebidas disponibles
        Menu menu = new Menu();
        menu.setComida(this.jComboBox1.getSelectedItem().toString());
        menu.setBebida(this.jComboBox2.getSelectedItem().toString());
        menu.setPostre(this.jComboBox3.getSelectedItem().toString());
        Persona persona = new Persona();
        persona.setNombre(this.jTextField1.getText());
        menu.setCliente(persona.getNombre());
        menu.setNoRepartidor(this.jComboBox4.getSelectedItem().toString());
        // Agregamos los datos a la lista
        listaMenu.add(menu);
        // Mostramos a traves de un metodo los datos en la tabla
        mostrarDatosComidaTabla();
    }//GEN-LAST:event_jButton1MouseClicked

    private void jButton2MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton2MouseClicked
        // TODO add your handling code here:
        // Funcionamiento para poder editar los datos ya establecidos en la tabla
        Menu menu = new Menu();
        menu.setComida(this.jComboBox1.getSelectedItem().toString());
        menu.setBebida(this.jComboBox2.getSelectedItem().toString());
        menu.setPostre(this.jComboBox3.getSelectedItem().toString());
        menu.setCliente(this.jTextField1.getText());
        menu.setNoRepartidor(this.jComboBox4.getSelectedItem().toString());
        int i = 0;
        int opcion = JOptionPane.showConfirmDialog(null, "¿Estás seguro de editar?", "Confirmación", JOptionPane.OK_CANCEL_OPTION);
        if (opcion == JOptionPane.OK_OPTION) {
            for (Menu menu1 : listaMenu) {
                if (menu1.getPostre().compareTo(menu.getPostre()) == 0) {
                    listaMenu.set(i, menu);
                    mostrarDatosComidaTabla();
                    break;
                }
                i++;
            }
        }
    }//GEN-LAST:event_jButton2MouseClicked

    private void jButton3MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton3MouseClicked
        // TODO add your handling code here:
        // Eliminamos datos de la tabla
        String postre = jTable1.getValueAt(jTable1.getSelectedRow(), 2).toString();
        // Pero le damos la opcion de cancelar o continuar con la emiminacion a travez de un JOPtionPane
        int opcion = JOptionPane.showConfirmDialog(null, "¿Estás seguro de eliminar?", "Confirmación", JOptionPane.OK_CANCEL_OPTION);
        if (opcion == JOptionPane.OK_OPTION) {
            for (Menu menu : listaMenu) {
                if (menu.getPostre().compareTo(postre) == 0) {
                    listaMenu.remove(menu);
                    mostrarDatosComidaTabla();
                    break;
                }
            }
        }
    }//GEN-LAST:event_jButton3MouseClicked

