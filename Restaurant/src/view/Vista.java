    // Método para poder mostrar los datos en la tabla
    public void mostrarDatosComidaTabla() {
        modelo.setRowCount(0);
        // Recorremos la lista y en cada posision de la tabla obtenemos las comidas
        for (Menu menu : listaMenu) {
            Object[] fila = new Object[5];
            fila[0] = menu.getComida();
            fila[1] = menu.getBebida();
            fila[2] = menu.getPostre();
            fila[3] = menu.getCliente();
            fila[4] = menu.getNoRepartidor();
            modelo.addRow(fila);
        }
    }

